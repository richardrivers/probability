If reality is indeed flexible, our definitions ought to reconcile this. Each of our mental definitions exist as an aggregate of correlations to other definitions.
These correlations, if probablistic, should contain weights. Grass = [(green: 0.2), (plant: 0.9), ...].
These weights, or probabilites, express the accuracy of a given correlation to the definition which it constructs.
Grass is almost always a plant, thus this correlation would have a high weight, but it is much less frequently green. Plato's "Forms", or weights of 1, do not exist in a probablistic model of reality. 
Rather, a probability without a false case would be expressed aysmptotically as to not preclude undiscovered information.
